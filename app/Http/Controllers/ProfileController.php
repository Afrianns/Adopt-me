     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $valided = $request->validate([
            "name" => ['required', "min:5"],
            "username" => ['required', "min:5"],
            "email" => ['required', "min:10", "email:dns"],
        ]);

        User::where("username", $id)->update($valided);
        return redirect("/dashboard")->with("success", "User berhasil di Update");
    }

